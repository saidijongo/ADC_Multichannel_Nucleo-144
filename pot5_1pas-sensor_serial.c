void Read_ADC_Values(void) {
    // ADC1 - Potentiometers 1 and 2
    HAL_ADC_Start(&hadc1);
    HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY);
    adc1Values[0] = HAL_ADC_GetValue(&hadc1);

    HAL_ADC_PollForConversion(&hadc1, HAL_MAX_DELAY);
    adc1Values[1] = HAL_ADC_GetValue(&hadc1);
    HAL_ADC_Stop(&hadc1);

    // ADC2 - Potentiometers 3 and 4
    HAL_ADC_Start(&hadc2);
    HAL_ADC_PollForConversion(&hadc2, HAL_MAX_DELAY);
    adc2Values[0] = HAL_ADC_GetValue(&hadc2);

    HAL_ADC_PollForConversion(&hadc2, HAL_MAX_DELAY);
    adc2Values[1] = HAL_ADC_GetValue(&hadc2);
    HAL_ADC_Stop(&hadc2);

    // ADC3 - Potentiometer 5 and PAS Sensor
    HAL_ADC_Start(&hadc3);
    HAL_ADC_PollForConversion(&hadc3, HAL_MAX_DELAY);
    adc3Values[0] = HAL_ADC_GetValue(&hadc3);

    HAL_ADC_PollForConversion(&hadc3, HAL_MAX_DELAY);
    adc3Values[1] = HAL_ADC_GetValue(&hadc3);
    HAL_ADC_Stop(&hadc3);

    // Combine all values into one array for easier handling
    adcValues[0] = adc1Values[0];
    adcValues[1] = adc1Values[1];
    adcValues[2] = adc2Values[0];
    adcValues[3] = adc2Values[1];
    adcValues[4] = adc3Values[0];
    adcValues[5] = adc3Values[1];
}

void Send_ADC_Values_UART(void) {
    char buffer[100];
    snprintf(buffer, sizeof(buffer), "Pot1: %lu, Pot2: %lu, Pot3: %lu, Pot4: %lu, Pot5: %lu, PAS: %lu\r\n",
             adcValues[0], adcValues[1], adcValues[2], adcValues[3], adcValues[4], adcValues[5]);
    HAL_UART_Transmit(&huart2, (uint8_t*)buffer, strlen(buffer), HAL_MAX_DELAY);
}
